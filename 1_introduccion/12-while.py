lenguajes = ['Python', 'Java', 'C++', 'C#', 'JavaScript']   

i = 1 

while i <= 5: # mientras i sea menor o igual a 5
    print(i)
    i += 1
    # 1, 2, 3, 4, 5

    # El bucle while se ejecuta mientras la condición sea verdadera


i = 0
while i < len(lenguajes): # mientras i sea menor a la longitud de la lista lenguajes
    print(lenguajes[i])
    i += 1
    # Python, Java, C++, C#, JavaScript
    # El bucle while se ejecuta mientras la condición sea verdadera

    # El bucle for se utiliza para iterar sobre una secuencia de elementos